package space.br1440.platform.tracing.semconv.lint;

/**
 * Уровень серьёзности нарушения семантической конвенции.
 * <p>
 * Линтер по умолчанию завершает CLI с ненулевым кодом возврата только при наличии нарушений
 * уровня {@link #ERROR}. Уровень {@link #WARNING} информативный — нарушения публикуются в отчёте
 * без падения CI.
 */
public enum LintSeverity {

    /** Информационное замечание. */
    INFO,

    /** Предупреждение: рекомендация платформенного стандарта, нарушение не блокирует пайплайн. */
    WARNING,

    /** Ошибка: жёсткое нарушение, при наличии CLI завершается с ненулевым кодом. */
    ERROR
}
