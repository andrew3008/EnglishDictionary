package space.br1440.platform.tracing.api.span.spec;

/**
 * Отношение нового span'а к родительскому контексту трассировки.
 * <p>
 * Определяет структуру parent-child в дереве trace: будет ли span унаследован
 * от текущего активного span'а, начнёт новый trace или будет полностью изолирован.
 */
public enum SpanRelationship {

    /**
     * Span создаётся как дочерний к текущему активному span'у.
     * <p>
     * Передача {@code linkedTo}-ссылок при {@code CHILD} запрещена
     * и приводит к {@link IllegalStateException} при создании спецификации.
     */
    CHILD,

    /**
     * Span начинает новый trace, независимый от текущего активного span'а.
     * <p>
     * Допускает {@code linkedTo}-ссылки: span остаётся корневым, но фиксирует
     * causal links на другие trace'ы. Без ссылок span полностью изолирован
     * от текущего trace-контекста.
     */
    ROOT,

    /**
     * Span запускается полностью изолированно: без parent-ссылки и
     * без links на любые другие span'ы или trace'ы.
     * <p>
     * Предназначен для out-of-band операций, которые не должны попадать
     * в бизнес-trace вызывающего потока: фоновая очистка, диагностические
     * зонды, периодические задачи без бизнес-контекста.
     * <p>
     * Передача {@code linkedTo}-ссылок при {@code DETACHED} запрещена
     * и приводит к {@link IllegalStateException} при создании спецификации.
     */
    DETACHED
}