package space.br1440.platform.tracing.api.span.spec;

/**
 * Перечень допустимых оснований для использования декларативного {@link SpanSpec}
 * вместо типизированных span-builder'ов платформы
 * ({@code spans().operation(...)} / {@code spans().transport()...}).
 *
 * <p>Каждый экземпляр {@link SpanSpec} обязан объявить причину, по которой типизированный API
 * неприменим. Это позволяет платформенной команде проводить систематический аудит точек
 * отклонения и контролировать расширение покрытия typed builder'ов.
 *
 * <p>Набор значений является закрытым: значения-заглушки ({@code OTHER}, {@code UNKNOWN},
 * {@code CUSTOM}) намеренно исключены. Каждое основание должно относиться к одной из
 * верифицируемых категорий, перечисленных ниже.
 */
public enum SpanSpecReason {

    /**
     * Сетевой протокол или транспортный механизм не охвачен типизированным builder API платформы.
     *
     * <p>Применяется, когда для данного протокола типизированный builder ещё не реализован
     * и взаимодействие с ним требует ручного объявления span-контракта через {@link SpanSpec}.
     */
    UNSUPPORTED_PROTOCOL,

    /**
     * Внешняя библиотека или клиент не имеют платформенного типизированного builder'а.
     *
     * <p>Применяется при инструментировании сторонних зависимостей, для которых платформенная
     * интеграция отсутствует и не может быть выражена через существующий typed API.
     */
    UNSUPPORTED_LIBRARY,

    /**
     * Взаимодействие с унаследованной системой, семантика которого не может быть выражена
     * средствами типизированного builder API.
     *
     * <p>Применяется для интеграций с legacy-компонентами, протоколы и контракты которых
     * принципиально не вписываются в модель платформенных span-категорий.
     */
    LEGACY_INTEGRATION,

    /**
     * Граничный случай платформенной семантики, не покрытый ни одной из стандартных категорий
     * типизированных builder'ов.
     *
     * <p>Применяется, когда сценарий является частью платформенной инфраструктуры, однако
     * его семантика выходит за рамки существующих типизированных span-категорий.
     * Ожидается рассмотрение возможности расширения typed API по результатам аудита.
     */
    PLATFORM_EDGE_CASE,

    /**
     * Временное отклонение от типизированного API, введённое на ограниченный период
     * и требующее обязательного отслеживания.
     *
     * <p>Использование данного значения без непустого {@code reference(...)} является
     * нарушением платформенного контракта. Ссылка должна указывать на тикет, ADR
     * или иной отслеживаемый рабочий элемент с зафиксированным сроком устранения.
     */
    TEMPORARY_WORKAROUND

}