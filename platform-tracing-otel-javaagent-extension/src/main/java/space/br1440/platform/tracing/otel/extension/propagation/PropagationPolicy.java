package space.br1440.platform.tracing.otel.extension.propagation;

/**
 * Политика распространения платформенных заголовков.
 */
public enum PropagationPolicy {
    /** Не передавать платформенные заголовки никогда. */
    NEVER,
    
    /** Передавать платформенные заголовки только доверенным внутренним адресам. */
    INTERNAL_ONLY,
    
    /** Передавать заголовки доверенным адресам (алиас для INTERNAL_ONLY). */
    TRUSTED_ONLY,
    
    /** Передавать платформенные заголовки всегда (опасно для внешних интеграций). */
    ALWAYS
}
